package mx.com.amx.uno.util.dto;

public class HistoricalPushDTO {
	
	private String usuario;
	private String mensaje_push;
	private String url_nota;
	
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getMensaje_push() {
		return mensaje_push;
	}
	public void setMensaje_push(String mensaje_push) {
		this.mensaje_push = mensaje_push;
	}
	public String getUrl_nota() {
		return url_nota;
	}
	public void setUrl_nota(String url_nota) {
		this.url_nota = url_nota;
	}
	
	
	
	
}	
